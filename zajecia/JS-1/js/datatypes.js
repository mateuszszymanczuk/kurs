'use strict';

/* Typ liczbowy */

var pensja = 5000;
var premia = 2000;
var całkowitaKwota = pensja + premia;

console.log(całkowitaKwota);

/* Typ łańcuch znaków - string */

var pensjaStr = "5000";
var premiaStr = "2000";
var całkowitaKwotaStr = pensjaStr + premiaStr;

console.log(całkowitaKwotaStr);

/* Typ logiczny - boolean */

var czyJestSmog = true;
console.log(czyJestSmog);

/* Typ specjalny */

var niezdefiniowanaZmienna;
var nullowaZmienna = null;

console.log (niezdefiniowanaZmienna);
console.log (nullowaZmienna);

